/// <summary>
/// ExpertiseScheme refers to the organizational structure or approach that defines how work is focused and distributed among employees or teams within an organization.
/// </summary>
public enum ExpertiseScheme
{
    /// <summary>
    /// Specialization refers to a work focus scheme where employees or teams are assigned specific tasks, roles, or areas of expertise within an organization.
    /// </summary>
    Specialization,
    /// <summary>
    /// Generalization refers to a work focus scheme where employees or teams are assigned a broader range of tasks, roles, or areas of expertise within an organization, allowing for more flexibility and adaptability in their work assignments.
    /// </summary>
    Generalization
}
