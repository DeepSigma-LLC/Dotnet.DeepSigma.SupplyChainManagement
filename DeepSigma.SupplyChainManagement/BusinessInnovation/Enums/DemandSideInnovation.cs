namespace DeepSigma.SupplyChainManagement.BusinessInnovation.Enums;

/// <summary>
/// Demand side innovation refers to the various ways in which companies can innovate to meet the changing needs and preferences of customers.
/// These innovations can be charted into a Customer Value Curve, which is a visual representation of the different factors that customers consider when making purchasing decisions and how a company can differentiate itself from competitors by offering unique value in those areas.
/// </summary>
public enum DemandSideInnovation
{
    /// <summary>
    /// Price innovation refers to the various ways in which companies can innovate to offer competitive pricing to customers. 
    /// This can include strategies such as dynamic pricing, subscription models, bundling, and offering discounts or promotions. 
    /// By innovating in pricing, companies can attract price-sensitive customers and differentiate themselves from competitors who may not be able to offer the same level of value at a lower cost.
    /// Demand side innovations are only effective if the company can effectively coordinate the supply side to deliver the value promised to customers.
    /// </summary>
    Price,
    /// <summary>
    /// Preference fit innovation refers to the various ways in which companies can innovate to better align their products or services with customer preferences. 
    /// This can include strategies such as personalized recommendations, customization options, and targeted marketing. 
    /// By innovating in preference fit, companies can increase customer satisfaction and loyalty.
    /// </summary>
    PreferenceFit,
    /// <summary>
    /// Transactional efficiency innovation refers to the various ways in which companies can innovate to improve the efficiency of their transactions with customers. 
    /// This can include strategies such as reducing customer effort, minimizing time lapses, and streamlining processes. 
    /// By innovating in transactional efficiency, companies can enhance the overall customer experience and increase satisfaction.
    /// </summary>
    TransactionalEfficiencyCustomerEffort,
    /// <summary>
    /// Transactional efficiency innovation also includes minimizing time lapses in customer interactions. 
    /// This can involve strategies such as faster response times, quicker delivery, and reducing waiting periods. 
    /// By focusing on time efficiency, companies can improve customer satisfaction and loyalty.
    /// </summary>
    TransactionalEfficiencyTimeLapse,
    /// <summary>
    /// Conformance quality innovation refers to the various ways in which companies can innovate to ensure their products or services consistently meet established standards and specifications. 
    /// This can include strategies such as quality control, process improvements, and adherence to industry regulations. 
    /// By innovating in conformance quality, companies can build trust and reliability with customers.
    /// </summary>
    ConformanceQuality,
    /// <summary>
    /// Performance quality innovation refers to the various ways in which companies can innovate to enhance the performance and functionality of their products or services. 
    /// This can include strategies such as improving durability, efficiency, and effectiveness. 
    /// By innovating in performance quality, companies can differentiate themselves from competitors and provide superior value to customers.
    /// </summary>
    PerformanceQuality,
}
