

namespace DeepSigma.SupplyChainManagement.Enums;

/// <summary>
/// The three key aspects of a lean transformation are the operating system, management infrastructure, and mindsets and behaviors of employees.
/// Originally developed by John Shook, these aspects are essential for organizations to successfully implement lean principles and achieve sustainable improvements in efficiency, quality, and customer value.
/// </summary>
public enum KeyAspectsOfALeanTransformation
{
    /// <summary>
    /// Operating system refers to the processes, tools, and practices that an organization uses to manage its operations and deliver value to customers.
    /// It includes elements such as process analysis, standardized work, visual management, and continuous improvement processes that help to eliminate waste and improve efficiency.
    /// </summary>
    OperationgSystem,
    /// <summary>
    /// Management infrastructure refers to the organizational structures, roles, and responsibilities that support the implementation and sustainability of lean practices.
    /// It includes elements such as leadership commitment, performance management, and resource allocation that enable the organization to achieve its lean objectives.
    /// </summary>
    ManagementInfrastructure,
    /// <summary>
    /// Mindsets and behaviors of employees refer to the attitudes, beliefs, and actions of individuals within the organization that influence the success of lean initiatives.
    /// It includes elements such as employee engagement, problem-solving skills, and a continuous improvement mindset that drive the adoption and sustainability of lean practices.
    /// </summary>
    MindsetsAndBehaviorsOfEmployees
}
