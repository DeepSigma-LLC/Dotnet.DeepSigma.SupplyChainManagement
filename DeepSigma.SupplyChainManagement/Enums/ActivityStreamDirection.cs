
namespace DeepSigma.SupplyChainManagement.Enums;

/// <summary>
/// Specifies the direction of activity flow within a supply chain or production process.
/// </summary>
public enum ActivityStreamDirection
{
    /// <summary>
    /// Upstream refers to the flow of activities or processes that occur before a specific point in a supply chain or production process. 
    /// It typically involves activities related to sourcing raw materials, procurement, and initial stages of production.
    /// </summary>
    Upstream,
    /// <summary>
    /// Downstream refers to the flow of activities or processes that occur after a specific point in a supply chain or production process.
    /// </summary>
    Downstream
}
